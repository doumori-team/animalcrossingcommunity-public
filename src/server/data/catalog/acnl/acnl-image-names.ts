function normalizeName(name: string): string
{
	return name
		.toLowerCase()
		.replace(/'/g, '')
		.replace(/\./g, '')
		.replace(/\?/g, 'question')
		.replace(/é/g, 'e')
		.replace(/ \/ /g, '__')
		.replace(/\//g, '')
		.replace(/ /g, '_');
}

function toCreatureImageName(name: string): string
{
	return name
		.split(' ')
		.map((part) => part.charAt(0).toUpperCase() + part.slice(1).toLowerCase())
		.join('_');
}

